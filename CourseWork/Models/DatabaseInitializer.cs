        private static void GenerateOrders(RestarauntDbContext db)
        {
            if (db.Orders.Any()) return;

            const int count = 500;
            var customersCount = db.Customers.Count();
            var employeesCount = db.Employees.Count();

            for (var i = 0; i < count; i++)
                db.Add(new Order
                {
                    Date = GenerateRandomDate(),
                    Time = new DateTime(1, 1, 1, Random.Next(24), Random.Next(60), Random.Next(60)),
                    Cost = (Random.NextDouble() + 0.01) * 1000.0,
                    Payment = Random.Next() % 2 == 0 ? Order.PaymentType.Cash : Order.PaymentType.CreditCard,
                    Completed = Random.Next() % 2 == 0,
                    CustomerId = Random.Next(1, customersCount),
                    EmployeeId = Random.Next(1, employeesCount)
                });

            db.SaveChanges();
        }

