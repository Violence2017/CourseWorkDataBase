        private static void GenerateDishIngredients(RestarauntDbContext db)
        {
            if (db.DishIngredients.Any()) return;

            var dishesCount = db.Dishes.Count();
            var ingredientsCount = db.Ingredients.Count();

            for (var dishId = 1; dishId <= dishesCount; dishId++)
            {
                var dishIngredients = Random.Next(1, 7);
                for (var i = 0; i < dishIngredients; i++)
                    db.DishIngredients.Add(new DishIngredient
                    {
                        Count = Random.Next(1, 10),
                        DishId = dishId,
                        IngredientId = Random.Next(1, ingredientsCount)
                    });
            }

            db.SaveChanges();
        }

        private static DateTime GenerateRandomDate()
        {
            var start = new DateTime(2016, 1, 1);
            var range = (DateTime.Today - start).Days;
            return start.AddDays(Random.Next(range));
        }
